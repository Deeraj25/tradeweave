// FNV-1a style stable string hash -> unsigned 32-bit int.
// Deterministic: same input always yields the same number.
export function hashString(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}
